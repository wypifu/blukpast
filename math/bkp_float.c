BkpBool bkpFloatEq(float a, float b)
{
	return fabs(a - b) < EPSILON ? BKP_TRUE : BKP_FALSE;
}

BkpBool bkpFloatEq2(float a, float b, float epsilon)
{
	return fabs(a - b) < epsilon ? BKP_TRUE : BKP_FALSE;
}


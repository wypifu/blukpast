/*
this algorithm (k=33) was first reported by dan bernstein many years ago in comp.lang.c.
another version of this algorithm (now favored by bernstein)
uses xor: hash(i) = hash(i - 1) * 33 ^ str[i];
the magic of number 33 (why it works better than many other constants, prime or not)
has never been adequately explained.

size_t bkp_hash_bjb2(const char * str)
{
        unsigned long hash = 5381;
        int c;

        while (c = *str++)
            hash = ((hash << 5) + hash) + c; // hash * 33 + c

        return hash;
}
*/

size_t bkp_hash_jenkinsOAAT(void * key, size_t len)
{
    size_t hash, i;
	uint8_t * p = key;
    for(hash = i = 0; i < len; ++i)
    {
        hash += p[i];
        hash += (hash << 10);
        hash ^= (hash >> 6);
    }
    hash += (hash << 3);
    hash ^= (hash >> 11);
    hash += (hash << 15);
    return hash;
}




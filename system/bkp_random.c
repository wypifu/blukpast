void bkpRandomInit()
{
    srand((unsigned int)time(NULL));
}

/*_________________________________________________________________________________*/
float bkpRandomFloat01()
{
    return  (float)rand() / (float) RAND_MAX;
}

/*_________________________________________________________________________________*/
uint32_t bkpRandomUint32(uint32_t min, uint32_t max)
{
    return  rand() % max + min;
}

/*_________________________________________________________________________________*/
BkpVec4 bkpRandomColor()
{
    return bkpVec4(bkpRandomFloat01(), bkpRandomFloat01(), bkpRandomFloat01(), 1.0f);
}

/*_________________________________________________________________________________*/
BkpVec3 bkpRandomVec3(uint32_t min, uint32_t max)
{
    return bkpVec3(bkpRandomUint32(min, max), bkpRandomUint32(min, max), bkpRandomUint32(min, max));
}

/*_________________________________________________________________________________*/
BkpVec3 bkpRandomVec3Float01()
{
    return bkpVec3(bkpRandomFloat01(), bkpRandomFloat01(), bkpRandomFloat01());
}


size_t bkpUtf8Strlen(const char * str)
{
	size_t len = 0;
	for(size_t i = 0; ; ++i, ++len)
	{
		int32_t c = (int32_t)str[i];
		if(c == 0)
		{
			break;
		}

		if(c >= 0 && c < 0x7F) continue;

		if((c & 0xE0) == 0xC0) ++i;
		else if ((c & 0xF0) == 0xE0) i += 2;
		else if((c & 0xF8) == 0xF0) i += 3;
		//else if((c & 0xFC) == 0xF8) i += 4;
		//else if((c & 0xFE) == 0xFC) i += 5;
		else return 0;
	}

	return len;
}

